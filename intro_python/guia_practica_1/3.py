# Convertir longitudes de millas a Km. y de pulgadas a cm., si:
# 1 milla = 1.60935 Km.
# 1 pulgada = 2.534 cm


cantidadMillas = int(input("Ingrese el valor en millas que desea convertir: "))

print("Las millas ingresadas se convierten en", cantidadMillas * 1.60935 , "km")


cantidadPulgadas = int(input("Ingrese el valor en pulgadas que desea convertira: "))

print("Las pulgadas ingresadas se convierten en", cantidadPulgadas * 2.534 , "cm")
